public class Utility extends BasePage {
    //Utility Class extends to BasePage for the driver to finding locators

    /**
     * This method will click on element
     */
    public void clickOnElement(WebElement element) {
        element.click();
    }


    public void sendTextToElement(WebElement element, String str) {
        element.sendKeys(str);
    }

     // method for taking screen shots

    public static String getScreenshot(WebDriver driver, String screenshotName) {
        String dateName = new SimpleDateFormat("yyyyMMddhhmmss").format(new Date());
        TakesScreenshot ts = (TakesScreenshot) driver;
        File source = ts.getScreenshotAs(OutputType.FILE);

        // After execution, you could see a folder "FailedTestsScreenshots" under screenshot folder
        String destination = System.getProperty("user.dir") + "/src/main/java/com/facebook/screenshots/" + screenshotName + dateName + ".png";
        File finalDestination = new File(destination);
        try {
            FileUtils.copyFile(source, finalDestination);
        } catch (IOException e) {
            e.printStackTrace();
        }
        return destination;
    }

}
